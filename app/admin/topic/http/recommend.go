package http

import (
	"valerian/app/admin/topic/model"
	"valerian/library/ecode"
	"valerian/library/net/http/mars"
)

// @Summary 添加推荐话题
// @Description 添加推荐话题
// @Tags admin
// @Accept json
// @Produce json
// @Param Authorization header string true "Bearer"
// @Param Source header int true "Source 来源，1:Web, 2:iOS; 3:Android" Enums(1, 2, 3)
// @Param Locale header string true "语言" Enums(zh-CN, en-US)
// @Param req body  app.admin.topic.model.ArgAddRecommendTopic true "请求"
// @Success 200 "成功"
// @Failure 400 "验证请求失败"
// @Failure 401 "登录验证失败"
// @Failure 500 "服务器端错误"
// @Router /admin/topic/recommend/add [post]
func addRecommendTopic(c *mars.Context) {
	arg := new(model.ArgAddRecommendTopic)
	if e := c.Bind(arg); e != nil {
		return
	}

	if e := arg.Validate(); e != nil {
		c.JSON(nil, ecode.RequestErr)
		return
	}

	c.JSON(nil, srv.AddRecommendTopic(c, arg))
}

